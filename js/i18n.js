// 偵測使用者語言
const userLang = navigator.language || navigator.userLanguage;
let currentLang = 'en';
if (userLang.startsWith('zh')) {
  currentLang = (userLang.includes('CN') || userLang.includes('Hans')) ? 'zh-CN' : 'zh-TW';
}

// 全域翻譯函式 (支援變數替換 {0})
window.i18n = (key, ...args) => {
  let str = messages[currentLang][key] || messages['en'][key] || key;
  args.forEach((arg, i) => {
    str = str.replace(`{${i}}`, arg);
  });
  return str;
};

// 自動翻譯 HTML 標籤
document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = window.i18n(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = window.i18n(el.getAttribute('data-i18n-placeholder'));
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = window.i18n(el.getAttribute('data-i18n-title'));
  });
});